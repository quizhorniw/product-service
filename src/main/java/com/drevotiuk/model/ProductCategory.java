package com.drevotiuk.model;

/**
 * Represents the various categories that a product can belong to.
 * <p>
 * This enum defines a set of predefined categories for products, which can be
 * used to classify products
 * in different domains such as electronics, clothing, beauty, and more.
 * </p>
 */
public enum ProductCategory {
  /**
   * Represents electronic products such as phones, laptops, and other gadgets.
   */
  ELECTRONICS,

  /**
   * Represents clothing items such as shirts, pants, and jackets.
   */
  CLOTHING,

  /**
   * Represents beauty products such as cosmetics and skincare items.
   */
  BEAUTY,

  /**
   * Represents sports equipment and apparel.
   */
  SPORTS,

  /**
   * Represents toys and games for children and adults.
   */
  TOYS,

  /**
   * Represents health-related products such as supplements and medical supplies.
   */
  HEALTH,

  /**
   * Represents products for pets, including food, toys, and accessories.
   */
  PET_SUPPLIES,

  /**
   * Represents cars products such as tiers and other car parts.
   */
  AUTOMOTIVE
}
